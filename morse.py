def ascii_to_morse(ascii_string):
    """ 
    Given a normal sentence, this will return the morse code for it, with 
    letters seperated by spaces and words seperated by / 
    """
    morse = ""
    for char in ascii_string:
        morse += morse_alphabet[char] + " "
    return morse

def morse_to_ascii(morse_string):
    """
    Expects a string of morse characters seperated by spaces with the words 
    seperated by /
    """
    ascii = ""
    for code in morse_string.split():
        translated = None
        # backwards dictionary lookup
        for key in morse_alphabet.keys():
            if (morse_alphabet[key] == code):
                translated = key
        assert translated is not None
        ascii += translated
    return ascii
